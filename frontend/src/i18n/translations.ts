export function getTranslation(lang: LanguageCode, key: string): string {
  const langMap = translations[lang];
  if (langMap && key in langMap) return langMap[key];
  // Fallback to English
  const enMap = translations['en'];
  if (enMap && key in enMap) return enMap[key];
  return key;
}

